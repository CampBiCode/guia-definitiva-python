# Sintáxis de los while loops
contador = 0
while contador < 10:
    print(contador)
    contador += 1
    


# Ejemplo de while loops
x = 1
while x % 2 != 0:
    print(x)
    x += 2



# Ejemplo de while loops con datos de entrada
estado = "cansado"
while estado == "cansado":
    print("No vamos a salir")
    estado = input("¿Cuál es tu estado?")
